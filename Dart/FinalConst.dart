void main(List<String> args) {
  final listaDeLinguagens = ["dart", "C#"];
  // final aceita atribuir somente uma vez, mas os valores podem
  // ser alterados
  listaDeLinguagens.add("java");

  print(listaDeLinguagens);

  const lista = ['lde', "tst"];
 // const atribuir somente uma vez e não aceita alteração de valores
  print(lista);
}